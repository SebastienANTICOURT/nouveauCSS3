let numberOne = parseInt(prompt("numberOne"));
console.log(numberOne);
let sign = prompt("sign");
console.log(sign);
let numberTwo = parseInt(prompt("numberTwo"));
console.log(numberTwo);

if (sign === "+"){
console.log(numberOne + numberTwo);
}
else if (sign === "-"){
    console.log(numberOne - numberTwo);
}
else if (sign === "*"){
    console.log(numberOne * numberTwo);
}
else if (sign ==="/"){
    console.log(numberOne / numberTwo);
}


